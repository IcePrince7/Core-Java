package Basic;

public class HCL_Ascii2 {

	public static void main(String[] args) {
		System.out.println("The ASCILL values of Captial A to Z is ");
		for (int i = 'A'; i <= 'Z'; i++)
			System.out.println("Char " + (char) i + " ASCII values is " + i);
		System.out.println("------------------------------");
		System.out.println("The ASCILL values of Small a to z is ");
		for (int i = 'a'; i <= 'z'; i++)
			System.out.println("Char " + (char) i + " ASCII values is " + i);
	}
}
